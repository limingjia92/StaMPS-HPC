#!/bin/bash

# ==============================================================================
# Script Name: prep_stamps_isce.sh
# Description: Prepares ISCE output for StaMPS processing with parallel patching.
#
# Original Author: David Bekaert, February 2016
# Optimization & Refactoring: Mingjia Li, December 2025
# 
# Note on Parallelization:
# This script supports parallel processing via 'jobmaxnum'. However, if using
# mechanical hard drives (HDD), increasing job concurrency may NOT improve 
# speed due to I/O bottlenecks.
# ==============================================================================

# --- Function: Print Usage and Exit ---
print_usage() {
    echo "Usage: $(basename "$0") data_dir work_dir [da_thresh] [rg_patches] [az_patches] [jobmaxnum] [rg_overlap] [az_overlap] [maskfile]"
    echo ""
    echo "Arguments:"
    echo "  data_dir          : Path to input data directory (e.g., INSAR_20220104 or SMALL_BASELINES)"
    echo "  work_dir          : Path to output working directory"
    echo "  da_thresh         : Amplitude dispersion threshold (Default: 0.4 for PS, 0.6 for SB)"
    echo "  rg_patches        : Number of patches in Range direction (Default: 1)"
    echo "  az_patches        : Number of patches in Azimuth direction (Default: 1)"
    echo "  jobmaxnum         : Max parallel jobs (Default: 1)"
    echo "  rg_overlap        : Overlapping pixels in Range (Default: 400)"
    echo "  az_overlap        : Overlapping pixels in Azimuth (Default: 400)"
    echo "  maskfile          : (Optional) Binary mask file"
    echo ""
    echo "Selective Patch Processing (Advanced):"
    echo "  If you wish to process only a specific subset of patches:"
    echo "  1. Use the MATLAB tool 'plot_patch_kml_isce' to visualize patch layout and identify IDs."
    echo "  2. Create or edit a file named 'patch.list' inside 'work_dir'."
    echo "  3. List only the Patch directories you want to process (e.g., PATCH_1, PATCH_5)."
    echo "  4. Run this script. It will detect the existing 'patch.list' and ONLY process the listed patches."
    echo ""
    echo "Expected Data Directory Structure:"
    echo "  This script expects the standard ISCE-StaMPS stack structure generated by"
    echo "  'make_single_reference_stack_isce' or 'make_small_baselines_isce'."
    exit 1
}

# --- Check Arguments ---
if [ $# -lt 2 ]; then
    print_usage
fi

# --- Initialize Variables ---
DATA_DIR=$(readlink -f "$1") # Get absolute path
WORK_DIR=$(readlink -f "$2") # Get absolute path

# Optional Arguments with Defaults
DA_THRESH=${3:--}
PATCHES_RG=${4:--}; [[ "$PATCHES_RG" == "-" ]] && PATCHES_RG=1
PATCHES_AZ=${5:--}; [[ "$PATCHES_AZ" == "-" ]] && PATCHES_AZ=1
MAX_JOBS=${6:--};   [[ "$MAX_JOBS" == "-" ]] && MAX_JOBS=1
OVERLAP_RG=${7:--}; [[ "$OVERLAP_RG" == "-" ]] && OVERLAP_RG=400
OVERLAP_AZ=${8:--}; [[ "$OVERLAP_AZ" == "-" ]] && OVERLAP_AZ=400
MASK_FILE=${9:-""}

TIME_START=$(date +"%s")

echo "================================================================="
echo "   StaMPS Pre-processor (ISCE Interface) - HPC Optimized"
echo "   Base Logic: David Bekaert (2016)"
echo "   Parallelization: Mingjia Li (2025)"
echo "================================================================="
echo "Data Dir   : $DATA_DIR"
echo "Work Dir   : $WORK_DIR"
echo "Patches    : $PATCHES_RG (Range) x $PATCHES_AZ (Azimuth)"
echo "Max Jobs   : $MAX_JOBS"

# Check Mask File
if [ -n "$MASK_FILE" ]; then
    if [ ! -e "$MASK_FILE" ]; then
        echo "Error: Mask file $MASK_FILE does not exist."
        exit 2
    fi
    # Get relative path for python calls later if needed, or absolute
    echo "Mask File  : $MASK_FILE"
fi

# Ensure Work Directory
mkdir -p "$WORK_DIR"
cd "$WORK_DIR" || exit 1

# --- Detect Mode (PS or SB) ---
DIR_NAME=$(basename "$DATA_DIR")

if [ "$DIR_NAME" == "SMALL_BASELINES" ]; then
    echo "Mode       : Small Baselines (SB)"
    SB_FLAG=1
else
    echo "Mode       : Persistent Scatterers (PS)"
    SB_FLAG=0
fi

# Set Threshold if not provided
if [ "$DA_THRESH" == "-" ]; then
    if [ $SB_FLAG -eq 1 ]; then
        DA_THRESH=0.6
    else
        DA_THRESH=0.4
    fi
fi
echo "Amp Thresh : $DA_THRESH"

# --- Step 1: Extract Info (Merged mt_extract_info_isce) ---
echo "-----------------------------------------------------------------"
echo "Step 1: Extracting Information and Metadata"

# Define processor type
echo "isce" > processor.txt

# Extract Date and Baseline info
# ISCE structure stores baseline info in specific files
echo "Extracting baseline and date info..."
grep ' ' "$DATA_DIR"/[0-9]*/baseline | gawk '{print $2}' > bperp.1.in
grep ' ' "$DATA_DIR"/[0-9]*/baseline | gawk 'BEGIN {FS = ":"}{print substr($(NF),1,8) }' > day.1.in
grep ' ' "$DATA_DIR"/reference/baseline | gawk '{print $1}' > reference_day.1.in

cp "$DATA_DIR/lambda.1.in" .
cp "$DATA_DIR/heading.1.in" . 2>/dev/null # Optional
cp "$DATA_DIR/len.txt" .
cp "$DATA_DIR/width.txt" .

# Set oversampling factor (default 1 for ISCE usually)
echo "1" > slc_osfactor.1.in
    
if [ $SB_FLAG -eq 1 ]; then
    # Create ifgday.1.in for SB
    ls "$DATA_DIR"/*/isce_minrefdem.int | \
    gawk 'BEGIN {FS = "/"}{print substr($(NF-1),1,8) " " substr($(NF-1),10,8)}' > ifgday.1.in
fi

# Read Dimensions
# ISCE processing (make_single_reference_stack) creates these files
if [ -f "$DATA_DIR/width.txt" ]; then
    IMG_WIDTH=$(cat "$DATA_DIR/width.txt")
    IMG_LENGTH=$(cat "$DATA_DIR/len.txt")
elif [ -f "width.txt" ]; then
    IMG_WIDTH=$(cat "width.txt")
    IMG_LENGTH=$(cat "len.txt")
else
    echo "Error: width.txt or len.txt not found. Did you run make_single_reference_stack_isce?"
    exit 1
fi

echo "Dimensions : $IMG_WIDTH (Width) x $IMG_LENGTH (Length)"

# Run Matlab Initialization
echo "Running Matlab parameter initialization..."
if [ $SB_FLAG -eq 1 ]; then
    matlab -nojvm -nosplash -nodisplay -r "sb_parms_initial; exit" > sb_parms_initial.log
else
    matlab -nojvm -nosplash -nodisplay -r "ps_parms_initial; exit" > ps_parms_initial.log
fi

# --- Step 2: Calibrate Amplitudes ---
echo "-----------------------------------------------------------------"
echo "Step 2: Amplitude Calibration (calamp)"

rm -f calamp.in 2>/dev/null

if [ $SB_FLAG -eq 1 ]; then
    # SB: Match pattern YYYYMMDD/YYYYMMDD.slc
    ls "$DATA_DIR"/*/*.slc 2>/dev/null | grep -E "/[0-9]{8}/[0-9]{8}\.slc$" | sort > calamp.in
    SEL_FILE="selsbc.in"
else
    # PS: Reference SLC first, then Secondaries
    ls "$DATA_DIR"/reference/reference.slc > calamp.in
    ls "$DATA_DIR"/*/secondary.slc >> calamp.in
    SEL_FILE="selpsc.in"
fi

if [ -s "calamp.out" ]; then
    echo "Skipping calamp (calamp.out exists)."
else
    echo "Running calamp..."
    # ISCE data is usually Float (f), system endian (Little Endian).
    # calamp syntax: calamp <input_list> <width> <output>
    
    # Optimization: Set OpenMP threads to 4 to speed up reading SLCs
    export OMP_NUM_THREADS=4
    calamp calamp.in "$IMG_WIDTH" calamp.out > calamp.log
    sort calamp.out -o calamp.out 
fi

# Prepare selection input file
rm -f "$SEL_FILE" 2>/dev/null
echo "$DA_THRESH" > "$SEL_FILE"
echo "$IMG_WIDTH" >> "$SEL_FILE"
cat calamp.out >> "$SEL_FILE"

# --- Step 3: Patch Division ---
echo "-----------------------------------------------------------------"
echo "Step 3: Dividing Image into Patches"

WIDTH_P=$((IMG_WIDTH / PATCHES_RG))
LENGTH_P=$((IMG_LENGTH / PATCHES_AZ))
echo "Patch Size : $WIDTH_P x $LENGTH_P pixels (approx)"

# Check if patch.list exists to determine mode
if [ -f "patch.list" ]; then
    echo "Found existing patch.list. Mode: Check and Fill Missing Directories."
    EXISTING_LIST_MODE=1
else
    echo "No patch.list found. Mode: Create New List and Directories."
    rm -rf patch.list
    EXISTING_LIST_MODE=0
fi

PATCH_COUNT=0

for ((irg=1; irg<=PATCHES_RG; irg++)); do
    for ((iaz=1; iaz<=PATCHES_AZ; iaz++)); do
        PATCH_COUNT=$((PATCH_COUNT + 1))
        CURRENT_PATCH_DIR="PATCH_$PATCH_COUNT"
        
        # --- Decision Logic: Should we process this patch? ---
        PROCESS_THIS_PATCH=0
        
        if [ $EXISTING_LIST_MODE -eq 1 ]; then
            # 1. Existing List Mode
            # Check if this patch name is in the existing file (exact match)
            if grep -Fxq "$CURRENT_PATCH_DIR" patch.list; then
                if [ -d "$CURRENT_PATCH_DIR" ]; then
                    # Dir exists, skip
                    # echo "  $CURRENT_PATCH_DIR exists. Skipping."
                    continue
                else
                    # In list but dir missing, need to create
                    echo "  $CURRENT_PATCH_DIR found in list but missing on disk. Generating..."
                    PROCESS_THIS_PATCH=1
                fi
            else
                # Not in the existing list (user might have removed it), skip
                continue
            fi
        else
            # 2. New List Mode (Original Behavior)
            echo "$CURRENT_PATCH_DIR" >> patch.list
            PROCESS_THIS_PATCH=1
            # Check dir to avoid error, though usually we'd overwrite
            if [ -d "$CURRENT_PATCH_DIR" ]; then
                # Optional: warn if overwriting, currently we just regenerate contents
                : 
            fi
        fi

        # --- Generation Logic (Only runs if PROCESS_THIS_PATCH=1) ---
        if [ $PROCESS_THIS_PATCH -eq 1 ]; then
            
            # Calculate Range Indices
            START_RG1=$((WIDTH_P * (irg - 1) + 1))
            START_RG=$((START_RG1 - OVERLAP_RG))
            [[ $START_RG -lt 1 ]] && START_RG=1
            
            END_RG1=$((WIDTH_P * irg))
            END_RG=$((END_RG1 + OVERLAP_RG))
            [[ $END_RG -gt $IMG_WIDTH ]] && END_RG=$IMG_WIDTH
            
            # Calculate Azimuth Indices
            START_AZ1=$((LENGTH_P * (iaz - 1) + 1))
            START_AZ=$((START_AZ1 - OVERLAP_AZ))
            [[ $START_AZ -lt 1 ]] && START_AZ=1
            
            END_AZ1=$((LENGTH_P * iaz))
            END_AZ=$((END_AZ1 + OVERLAP_AZ))
            [[ $END_AZ -gt $IMG_LENGTH ]] && END_AZ=$IMG_LENGTH

            # Create Patch Directory if missing
            if [ ! -d "$CURRENT_PATCH_DIR" ]; then
                mkdir "$CURRENT_PATCH_DIR"
            fi

            # Write patch parameters
            cd "$CURRENT_PATCH_DIR" || exit 1
            
            # patch.in (with overlaps)
            printf "%s\n%s\n%s\n%s\n" "$START_RG" "$END_RG" "$START_AZ" "$END_AZ" > patch.in
            
            # patch_noover.in (without overlaps)
            printf "%s\n%s\n%s\n%s\n" "$START_RG1" "$END_RG1" "$START_AZ1" "$END_AZ1" > patch_noover.in
            
            cd "$WORK_DIR" || exit 1
        fi
    done
done

# --- Step 4: Prepare Global Input Files ---
echo "-----------------------------------------------------------------"
echo "Step 4: Preparing Global Input Files (ISCE specific)"

# 1. pscphase.in (Points to interferograms)
echo "$IMG_WIDTH" > pscphase.in
# ISCE files are named isce_minrefdem.int
ls "$DATA_DIR"/*/isce_minrefdem.int >> pscphase.in

# 2. psclonlat.in (Points to geometry files)
# In ISCE stack, these are usually raw binary files in the DATA_DIR
echo "$IMG_WIDTH" > psclonlat.in
if [ -f "$DATA_DIR/lon.raw" ] && [ -f "$DATA_DIR/lat.raw" ]; then
    echo "$DATA_DIR/lon.raw" >> psclonlat.in
    echo "$DATA_DIR/lat.raw" >> psclonlat.in
else
    echo "Error: lon.raw or lat.raw not found in $DATA_DIR"
    exit 1
fi

# 3. pscdem.in (Points to DEM)
echo "$IMG_WIDTH" > pscdem.in
if [ -f "$DATA_DIR/dem.raw" ]; then
    echo "$DATA_DIR/dem.raw" >> pscdem.in
else
    echo "Error: dem.raw not found in $DATA_DIR"
    exit 1
fi

# 4. link file for StaMPS
GEOM_BASENAMES="inc_angle.raw heading.raw" 

for NAME in $GEOM_BASENAMES; do
    SOURCE_FILE="$DATA_DIR/$NAME"
    if [ -f "$SOURCE_FILE" ]; then
        echo "Linking $NAME..."
        ln -sf "$SOURCE_FILE" . 
        
        if [ -f "$SOURCE_FILE.xml" ]; then
             echo "Linking $NAME.xml ..."
             ln -sf "$SOURCE_FILE.xml" .
        fi

        if [ -f "$SOURCE_FILE.vrt" ]; then
             echo "Linking $NAME.vrt ..."
             ln -sf "$SOURCE_FILE.vrt" .
        fi
    fi
done

# --- Step 5: Parallel Execution (Merged mt_extract_cands) ---
echo "-----------------------------------------------------------------"
echo "Step 5: Parallel Processing (Max Jobs: $MAX_JOBS)"

# ISCE usually produces floats (f) and is mostly Little Endian (no swap needed usually, but checked script defaults)
PRECISION="f"
BYTESWAP=0 # Default for ISCE is usually 0
LIST_FILE="patch.list"
TOTAL_PATCHES=$(wc -l < "$WORK_DIR/$LIST_FILE")

# FIFO Setup for concurrency control
FIFO_NAME="tempfifo_$$"
trap "rm -f $FIFO_NAME; exit 1" INT TERM
mkfifo "$FIFO_NAME"
exec 1001<>"$FIFO_NAME"
rm -f "$FIFO_NAME"

# Pre-fill FIFO tokens
for ((i=0; i<MAX_JOBS; i++)); do echo >&1001; done

echo "Processing $TOTAL_PATCHES patches..."

for ((i=1; i<=TOTAL_PATCHES; ++i)); do
    PATCH_NAME=$(sed -n "${i}p" "$WORK_DIR/$LIST_FILE")
    
    # Wait for a token
    read -u1001
    
    {
        cd "$WORK_DIR/$PATCH_NAME" || exit 1
        LOG_FILE="mt_${PATCH_NAME}.log"
        rm -f "$LOG_FILE"
        
        echo "Processing $PATCH_NAME..." | tee -a "$LOG_FILE"
        
        # 1. Select Candidates (selpsc/selsbc)
        if [ $SB_FLAG -eq 1 ]; then
             CMD="selsbc_patch $WORK_DIR/selsbc.in patch.in pscands.1.ij pscands.1.da mean_amp.flt $PRECISION $BYTESWAP $MASK_FILE"
        else
             CMD="selpsc_patch $WORK_DIR/selpsc.in patch.in pscands.1.ij pscands.1.da mean_amp.flt $PRECISION $BYTESWAP $MASK_FILE"
        fi
        
        # Clean command
        CMD=$(echo "$CMD" | xargs)
        echo "$CMD" >> "$LOG_FILE"
        $CMD >> "$LOG_FILE" 2>&1
        
        # Calculate PS number
        if [ -f "pscands.1.ij" ]; then
            ps_num=$(wc -l < pscands.1.ij)
        else
            ps_num=0
        fi
        
        echo "" >> "$LOG_FILE"
        echo "Total $ps_num PS candidates extracted" >> "$LOG_FILE"

        # 2. Extract Lon/Lat (psclonlat)
        echo "" >> "$LOG_FILE"
        echo "psclonlat $WORK_DIR/psclonlat.in pscands.1.ij pscands.1.ll" >> "$LOG_FILE"
        psclonlat "$WORK_DIR/psclonlat.in" pscands.1.ij pscands.1.ll >> "$LOG_FILE" 2>&1

        # 3. Extract Height (pscdem)
        echo "" >> "$LOG_FILE"
        echo "pscdem $WORK_DIR/pscdem.in pscands.1.ij pscands.1.hgt" >> "$LOG_FILE"
        pscdem "$WORK_DIR/pscdem.in" pscands.1.ij pscands.1.hgt >> "$LOG_FILE" 2>&1

        # 4. Extract Phase (pscphase)
        echo "" >> "$LOG_FILE"
        echo "pscphase $WORK_DIR/pscphase.in pscands.1.ij pscands.1.ph" >> "$LOG_FILE"
        pscphase "$WORK_DIR/pscphase.in" pscands.1.ij pscands.1.ph >> "$LOG_FILE" 2>&1

        
        # Return token
        echo >&1001
        echo "$PATCH_NAME complete."
    } &
done

wait

# Cleanup FIFO
exec 1001>&-

# --- Final Reporting ---
TIME_END=$(date +"%s")
DURATION=$((TIME_END - TIME_START))

# Calculate Hours, Minutes, Seconds
HOURS=$((DURATION / 3600))
MINUTES=$(( (DURATION % 3600) / 60 ))
SECONDS=$((DURATION % 60))

echo "-----------------------------------------------------------------"
echo "Processing complete."
echo "Total Time Used: ${HOURS}h ${MINUTES}m ${SECONDS}s"

exit 0